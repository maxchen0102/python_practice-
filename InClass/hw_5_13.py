

math={"館長","博恩","愛莉莎莎","蔡阿嘎","泱泱","蔡哥"}  #數學及格的人

eng={"阿滴","啾啾鞋","蔡哥","博恩","HowHow"} #英文及格的名單 

tw={"館長","蔡阿嘎","蔡哥","素還真"} # 台語及格的名單 

total={"館長","博恩","愛莉莎莎","蔡阿嘎","泱泱","蔡哥","阿滴","啾啾鞋","HowHow","素還真"} #全部人的名單 
#total 為宇集 

print("數學及格且英文不及格的同學名單:",math & (total-eng))# 列出數學及格且英文不及格的同學名單
# 用所有人的名單 差集 英文及格的名單 就會得到英文不及格的人的名單 
print("數學不及格且英文及格的同學名單:",eng &(total -math))  
#英文及格的人 交集  全部人和數學及格的人的差集 會得到數學不及格且英文及格的同學名單:
print("三科都及格的同學名單:",math&eng&tw)
#名單上所有人取交集 會得到三科都及格的同學名單


